// Função para obter dados de um estado específico
export function getStateData(stateId: string) {
  return stateData.find((state) => state.id === stateId)
}

// Função para obter todos os estados de uma região
export function getStatesByRegion(regionId: string) {
  return stateData.filter((state) => state.region === regionId)
}

// Função para calcular estatísticas agregadas por região
export function getRegionStats(regionId: string) {
  const states = getStatesByRegion(regionId)

  if (states.length === 0) return null

  return {
    hospitals: states.reduce((sum, state) => sum + state.hospitals, 0),
    doctors: states.reduce((sum, state) => sum + state.doctors, 0),
    beds: states.reduce((sum, state) => sum + state.beds, 0),
    population: states.reduce((sum, state) => sum + state.population, 0),
    urbanAccessIndex: states.reduce((sum, state) => sum + state.urbanAccessIndex, 0) / states.length,
    ruralAccessIndex: states.reduce((sum, state) => sum + state.ruralAccessIndex, 0) / states.length,
    medicalEquipment: {
      mri: states.reduce((sum, state) => sum + state.medicalEquipment.mri, 0),
      ct: states.reduce((sum, state) => sum + state.medicalEquipment.ct, 0),
      xray: states.reduce((sum, state) => sum + state.medicalEquipment.xray, 0),
    },
  }
}
